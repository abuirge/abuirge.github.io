
/**
 * Direction contains enumerations for each direction that a Boardable object can be moved
 * @author Andrew Buirge
 *
 */
public enum Direction {

	/**
	 * UP_LEFT used for moving a Boardable object up one row and left one column
	 */
	UP_LEFT,
	/**
	 * UP used for moving a Boardable object up one row 
	 */
	UP,
	/**
	 * UP_RIGHT used for moving a Boardable object up one row and right one column
	 */
	UP_RIGHT,
	/**
	 * LEFT used for moving a Boardable object left one column
	 */
	LEFT,
	/**
	 * RIGHT used for moving a Boardable object right one column
	 */
	RIGHT,
	/**
	 * DOWN_LEFT used for moving a Boardable object down one row and left one column
	 */
	DOWN_LEFT,
	/**
	 * DOWN used for moving a Boardable object down one row
	 */
	DOWN,
	/**
	 * DOWN_RIGHT used for moving a Boardable object down one row and right one column
	 */
	DOWN_RIGHT;

}
